package Study;

/*
	●●● 자바의 개요 및 특징 ●●●
*/

public class Test001_1 {

	//main 메소드
	public static void main(String[] args) {
		
		// 메소드 안에 있는 문장이 순차적으로 진행된다.

		// 자바에서의 ;(세미콜론) => 종결자

		System.out.println("아무개");
		System.out.print("010-0000-1234");
		System.out.println("서울특별시 강남구 OOO OOO");

		// System.out.println(아무개);  ==> 에러 발생(컴파일 에러)
		// ==> 실행과정에서 발생하는 에러 = 런타임 에러

		System.out.println(100);

		System.out.println(3.14159);

		System.out.println(10 + 2);

		// System.out.println(빵);   ==> 컴파일 에러

		// System.out.println(A);    ==> 컴파일 에러
	}
}



